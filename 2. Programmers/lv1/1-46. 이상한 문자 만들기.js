function solution(s) {
  return s
    .toUpperCase()
    .split(" ")
    .map((str) =>
      [...str]
        .map((s, idx) => {
          if (idx % 2 === 1) return s.toLowerCase();
          return s;
        })
        .join("")
    )
    .join(" ");
}

